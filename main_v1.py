import numpy as np
import pandas as pd
from sentence_transformers import CrossEncoder
from tqdm.auto import tqdm


def cross_encoder_rerank(
    df: pd.DataFrame,
    model_name: str,
    batch_size: int = 64,
    top_k: int = 100,
    device: str = None
):
    """
    Cross-encoder reranking for ontology matching.

    Steps:
    - Group by SrcEntity
    - Score (SrcLabel, TgtLabel) pairs
    - Rerank per source
    """

    if device is None:
        import torch
        device = "cuda" if torch.cuda.is_available() else "cpu"

    # ==========================================================
    # LOAD MODEL
    # ==========================================================

    model = CrossEncoder(
        model_name,
        device=device
    )

    results = []

    # ==========================================================
    # GROUP BY SOURCE
    # ==========================================================

    grouped = df.groupby("SrcEntity")

    for src_entity, group in tqdm(grouped):

        src_label = group["SrcLabel"].iloc[0]

        tgt_labels = group["TgtLabel"].astype(str).tolist()
        tgt_entities = group["TgtEntity"].tolist()

        src_pairs = [
            (src_label, tgt_label)
            for tgt_label in tgt_labels
        ]

        # ======================================================
        # SCORE IN BATCHES
        # ======================================================

        scores = model.predict(
            src_pairs,
            batch_size=batch_size,
            show_progress_bar=False
        )

        scores = np.array(scores)

        scores = scores[:, 1]

        # ======================================================
        # SORT BY SCORE
        # ======================================================

        ranked_idx = np.argsort(-scores)

        for rank, idx in enumerate(ranked_idx[:top_k]):

            results.append({
                "SrcEntity": src_entity,
                "SrcLabel": src_label,
                "TgtEntity": tgt_entities[idx],
                "TgtLabel": tgt_labels[idx],
                "score": float(scores[idx]),
                "rank": rank + 1
            })

    return pd.DataFrame(results)

