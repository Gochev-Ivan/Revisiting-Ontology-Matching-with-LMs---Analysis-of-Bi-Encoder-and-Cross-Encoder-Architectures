import numpy as np
import pandas as pd
import torch

from dataclasses import dataclass
from tqdm.auto import tqdm

from sentence_transformers import SentenceTransformer


@dataclass
class RetrievalConfig:
    model_path: str
    batch_size: int = 64
    top_k: int = 10
    normalize_embeddings: bool = True
    device: str = None


class OntologyTopKRetriever:

    def __init__(self, config: RetrievalConfig):

        self.config = config

        self.device = (
            config.device
            if config.device
            else ("cuda" if torch.cuda.is_available() else "cpu")
        )

        print(f"\nUsing device: {self.device}")

        self.model = SentenceTransformer(
            config.model_path,
            device=self.device
        )

        self.tgt_embeddings = None
        self.tgt_df = None

    def encode_targets(self, df_tgt: pd.DataFrame):

        print("\nEncoding target ontology...")

        self.tgt_df = df_tgt.reset_index(drop=True)

        tgt_texts = self.tgt_df["TgtLabel"].astype(str).tolist()

        self.tgt_embeddings = self.model.encode(
            tgt_texts,
            batch_size=self.config.batch_size,
            convert_to_tensor=True,
            show_progress_bar=True,
            normalize_embeddings=self.config.normalize_embeddings
        )

        print(f"Target embeddings shape: {self.tgt_embeddings.shape}")

    def retrieve(self, df_src: pd.DataFrame) -> pd.DataFrame:

        if self.tgt_embeddings is None:
            raise ValueError("Call encode_targets() first.")

        print("\nEncoding source ontology...")

        src_labels = df_src["SrcLabel"].astype(str).tolist()
        src_entities = df_src["SrcEntity"].astype(str).tolist()

        src_embeddings = self.model.encode(
            src_labels,
            batch_size=self.config.batch_size,
            convert_to_tensor=True,
            show_progress_bar=True,
            normalize_embeddings=self.config.normalize_embeddings
        )

        print("\nComputing similarity matrix...")

        similarity_matrix = torch.matmul(
            src_embeddings,
            self.tgt_embeddings.T
        )

        top_k = self.config.top_k

        print("\nExtracting Top-K pairs into DataFrame...")

        rows = []

        for i in tqdm(range(len(df_src))):

            scores, indices = torch.topk(
                similarity_matrix[i],
                k=top_k
            )

            src_entity = src_entities[i]
            src_label = df_src.iloc[i]["SrcLabel"]

            for score, idx in zip(scores, indices):
                rows.append({
                    "SrcEntity": src_entity,
                    "SrcLabel": src_label,
                    "TgtEntity": self.tgt_df.iloc[int(idx.item())]["TgtEntity"],
                    "TgtLabel": self.tgt_df.iloc[int(idx.item())]["TgtLabel"],
                    "score": float(score)
                })

        return pd.DataFrame(rows)

    def run(self, df_src: pd.DataFrame, df_tgt: pd.DataFrame) -> pd.DataFrame:

        self.encode_targets(df_tgt)

        return self.retrieve(df_src)

