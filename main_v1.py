import torch
import pandas as pd
from sentence_transformers import SentenceTransformer
from tqdm.auto import tqdm


def rank_targets_for_sources(
    df,
    model_name,
    top_k=100,
    embedding_batch_size=512,
    search_batch_size=1024,
    device=None,
):

    if device is None:
        device = (
            "cuda"
            if torch.cuda.is_available()
            else "cpu"
        )

    # =====================================================
    # UNIQUE ENTITIES
    # =====================================================

    src_df = (
        df[
            ["SrcEntity", "SrcLabel"]
        ]
        .drop_duplicates("SrcEntity")
        .reset_index(drop=True)
    )

    tgt_df = (
        df[
            ["TgtEntity", "TgtLabel"]
        ]
        .drop_duplicates("TgtEntity")
        .reset_index(drop=True)
    )

    print(
        f"Sources: {len(src_df):,}"
    )

    print(
        f"Targets: {len(tgt_df):,}"
    )

    # =====================================================
    # MODEL
    # =====================================================

    model = SentenceTransformer(
        model_name,
        device=device
    )

    # =====================================================
    # ENCODE SOURCES
    # =====================================================

    src_emb = model.encode(
        src_df["SrcLabel"]
        .astype(str)
        .tolist(),
        batch_size=embedding_batch_size,
        convert_to_tensor=True,
        normalize_embeddings=True,
        show_progress_bar=True,
    )

    # =====================================================
    # ENCODE TARGETS
    # =====================================================

    tgt_emb = model.encode(
        tgt_df["TgtLabel"]
        .astype(str)
        .tolist(),
        batch_size=embedding_batch_size,
        convert_to_tensor=True,
        normalize_embeddings=True,
        show_progress_bar=True,
    )

    tgt_emb = tgt_emb.to(device)

    # =====================================================
    # RETRIEVAL
    # =====================================================

    results = []

    for start in tqdm(
        range(
            0,
            len(src_emb),
            search_batch_size
        ),
        desc="Ranking"
    ):

        end = min(
            start + search_batch_size,
            len(src_emb)
        )

        src_batch = (
            src_emb[start:end]
            .to(device)
        )

        # cosine because embeddings normalized
        similarities = (
            src_batch @ tgt_emb.T
        )

        top_scores, top_indices = (
            torch.topk(
                similarities,
                k=min(
                    top_k,
                    len(tgt_df)
                ),
                dim=1
            )
        )

        top_scores = (
            top_scores
            .cpu()
            .numpy()
        )

        top_indices = (
            top_indices
            .cpu()
            .numpy()
        )

        for local_idx in range(
            len(top_indices)
        ):

            src_idx = (
                start + local_idx
            )

            src_entity = (
                src_df.iloc[src_idx]
                ["SrcEntity"]
            )

            src_label = (
                src_df.iloc[src_idx]
                ["SrcLabel"]
            )

            for rank, (
                tgt_idx,
                score
            ) in enumerate(
                zip(
                    top_indices[local_idx],
                    top_scores[local_idx]
                ),
                start=1
            ):

                results.append(
                    {
                        "SrcEntity":
                            src_entity,

                        "SrcLabel":
                            src_label,

                        "TgtEntity":
                            tgt_df.iloc[tgt_idx]
                            ["TgtEntity"],

                        "TgtLabel":
                            tgt_df.iloc[tgt_idx]
                            ["TgtLabel"],

                        "score":
                            float(score),

                        "rank":
                            rank
                    }
                )

        del similarities

        if device == "cuda":
            torch.cuda.empty_cache()

    return pd.DataFrame(results)

