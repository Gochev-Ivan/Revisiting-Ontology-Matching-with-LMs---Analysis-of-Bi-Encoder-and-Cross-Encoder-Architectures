class BiomedicalCrossEncoderTrainer:

    def __init__(
        self,
        config: CrossEncoderTrainingConfig
    ):

        self.config = config

        self.device = (
            "cuda"
            if torch.cuda.is_available()
            else "cpu"
        )

        print(f"\nUsing device: {self.device}")

        self._set_seed()

        self.model = self._load_model()

        self.tokenizer = (
            self.model.tokenizer
        )

        self.history = {
            "val_accuracy": [],
            "val_precision": [],
            "val_recall": [],
            "val_f1": [],
            "val_pr_auc": [],
            "val_roc_auc": []
        }

    def _set_seed(self):

        random.seed(self.config.random_seed)

        np.random.seed(
            self.config.random_seed
        )

        torch.manual_seed(
            self.config.random_seed
        )

        if torch.cuda.is_available():

            torch.cuda.manual_seed_all(
                self.config.random_seed
            )

    def _load_model(self):

        print("\nLoading CrossEncoder...")

        model = CrossEncoder(
            self.config.model_name,
            num_labels=2,
            max_length=self.config.max_length,
            device=self.device
        )

        return model

    def load_dataset(self):

        print("\nLoading dataset...")

        df = pd.read_csv(
            self.config.dataset_path
        )

        required_columns = [
            "SrcEntity",
            "TgtEntity",
            "SrcLabel",
            "TgtLabel",
            "label"
        ]

        for col in required_columns:

            if col not in df.columns:

                raise ValueError(
                    f"Missing column: {col}"
                )

        return df

    def prepare_datasets(self, df):

        print(
            "\nPreparing train/validation split..."
        )

        train_df, val_df = train_test_split(
            df,
            test_size=self.config.validation_split,
            stratify=df["label"],
            random_state=self.config.random_seed
        )

        return train_df, val_df

    def build_dataloaders(
        self,
        train_df,
        val_df
    ):

        train_dataset = OntologyMatchingDataset(
            train_df,
            self.tokenizer,
            self.config.max_length
        )

        val_dataset = OntologyMatchingDataset(
            val_df,
            self.tokenizer,
            self.config.max_length
        )

        train_loader = torch.utils.data.DataLoader(
            train_dataset,
            batch_size=self.config.batch_size,
            shuffle=True,
            num_workers=self.config.num_workers,
            pin_memory=self.config.pin_memory,
            persistent_workers=True
        )

        val_loader = torch.utils.data.DataLoader(
            val_dataset,
            batch_size=self.config.batch_size,
            shuffle=False,
            num_workers=self.config.num_workers,
            pin_memory=self.config.pin_memory,
            persistent_workers=True
        )

        return train_loader, val_loader

    def find_best_threshold(
        self,
        labels,
        probabilities
    ):

        precision, recall, thresholds = (
            precision_recall_curve(
                labels,
                probabilities
            )
        )

        f1_scores = (
            2 * precision * recall
        ) / (
            precision + recall + 1e-8
        )

        best_idx = np.argmax(f1_scores)

        if best_idx >= len(thresholds):
            return 0.5

        return thresholds[best_idx]

    @torch.no_grad()
    def evaluate(
        self,
        val_loader
    ):

        self.model.model.eval()

        all_labels = []
        all_probs = []

        for batch in tqdm(
            val_loader,
            desc="Validation"
        ):

            labels = (
                batch["labels"]
                .cpu()
                .numpy()
            )

            batch = {
                k: v.to(self.device)
                for k, v in batch.items()
                if k != "labels"
            }

            outputs = (
                self.model.model(**batch)
            )

            logits = outputs.logits

            probs = torch.softmax(
                logits,
                dim=1
            )[:, 1]

            all_probs.extend(
                probs.cpu().numpy()
            )

            all_labels.extend(labels)

        all_probs = np.array(all_probs)
        all_labels = np.array(all_labels)

        best_threshold = (
            self.find_best_threshold(
                all_labels,
                all_probs
            )
        )

        predictions = (
            all_probs >= best_threshold
        ).astype(int)

        metrics = {

            "accuracy":
                accuracy_score(
                    all_labels,
                    predictions
                ),

            "precision":
                precision_score(
                    all_labels,
                    predictions,
                    zero_division=0
                ),

            "recall":
                recall_score(
                    all_labels,
                    predictions,
                    zero_division=0
                ),

            "f1":
                f1_score(
                    all_labels,
                    predictions,
                    zero_division=0
                ),

            "roc_auc":
                roc_auc_score(
                    all_labels,
                    all_probs
                ),

            "pr_auc":
                average_precision_score(
                    all_labels,
                    all_probs
                ),

            "threshold":
                float(best_threshold)
        }

        return metrics

    def train(self):

        os.makedirs(
            self.config.output_dir,
            exist_ok=True
        )

        df = self.load_dataset()

        train_df, val_df = (
            self.prepare_datasets(df)
        )

        train_loader, val_loader = (
            self.build_dataloaders(
                train_df,
                val_df
            )
        )

        label_counts = (
            train_df["label"]
            .value_counts()
            .sort_index()
        )

        negatives = label_counts[0]
        positives = label_counts[1]

        total = negatives + positives

        # inverse frequency weighting
        class_weights = torch.tensor(
            [
                total / negatives,
                total / positives
            ],
            dtype=torch.float
        ).to(self.device)

        print("\nClass weights:")
        print(class_weights)

        criterion = WeightedFocalLoss(
            alpha=class_weights,
            gamma=self.config.focal_gamma
        )

        optimizer = torch.optim.AdamW(
            self.model.model.parameters(),
            lr=self.config.learning_rate,
            weight_decay=self.config.weight_decay
        )

        scaler = torch.cuda.amp.GradScaler(
            enabled=(
                self.device == "cuda"
                and self.config.fp16
            )
        )

        total_steps = (
            len(train_loader)
            * self.config.epochs
        )

        warmup_steps = int(
            total_steps * 0.1
        )

        scheduler = (
            torch.optim.lr_scheduler.CosineAnnealingLR(
                optimizer,
                T_max=total_steps
            )
        )

        best_pr_auc = 0.0

        patience_counter = 0

        best_model_path = os.path.join(
            self.config.output_dir,
            "best_model"
        )

        print("\nStarting training...")

        global_step = 0

        for epoch in range(
            self.config.epochs
        ):

            print(
                f"\nEpoch "
                f"{epoch + 1}/"
                f"{self.config.epochs}"
            )

            self.model.model.train()

            optimizer.zero_grad()

            running_loss = 0.0

            progress_bar = tqdm(
                train_loader,
                desc=f"Training Epoch {epoch+1}"
            )

            for step, batch in enumerate(
                progress_bar
            ):

                labels = (
                    batch["labels"]
                    .to(self.device)
                )

                batch = {
                    k: v.to(self.device)
                    for k, v in batch.items()
                    if k != "labels"
                }

                with torch.cuda.amp.autocast(
                    enabled=(
                        self.device == "cuda"
                        and self.config.fp16
                    )
                ):

                    outputs = (
                        self.model.model(**batch)
                    )

                    logits = outputs.logits

                    loss = criterion(
                        logits,
                        labels
                    )

                    loss = (
                        loss
                        / self.config.gradient_accumulation_steps
                    )

                scaler.scale(loss).backward()

                if (
                    (step + 1)
#                     % self.config.gradient_accumulation_steps
                    == 0
                ):

                    scaler.step(optimizer)

                    scaler.update()

                    optimizer.zero_grad()

                    scheduler.step()

                    global_step += 1

                running_loss += loss.item()

                progress_bar.set_postfix({
                    "loss":
                        running_loss / (step + 1)
                })

            metrics = self.evaluate(
                val_loader
            )

            print("\nValidation Metrics:")

            print(
                json.dumps(
                    metrics,
                    indent=2
                )
            )

            for key in self.history:

                metric_name = key.replace(
                    "val_",
                    ""
                )

                self.history[key].append(
                    metrics[metric_name]
                )

            current_pr_auc = (
                metrics["pr_auc"]
            )

            if current_pr_auc > best_pr_auc:

                best_pr_auc = current_pr_auc

                patience_counter = 0

                print(
                    "\nNew best model found."
                )

                self.model.save(
                    best_model_path
                )

            else:

                patience_counter += 1

                print(
                    f"\nNo improvement. "
                    f"Patience: "
                    f"{patience_counter}/"
                    f"{self.config.early_stopping_patience}"
                )

            if (
                patience_counter
                >= self.config.early_stopping_patience
            ):

                print(
                    "\nEarly stopping triggered."
                )

                break

        self.save_history()

        print(
            f"\nTraining completed.\n"
            f"Best model saved to:\n"
            f"{best_model_path}"
        )

    def save_history(self):

        history_path = os.path.join(
            self.config.output_dir,
            "training_history.json"
        )

        with open(history_path, "w") as f:

            json.dump(
                self.history,
                f,
                indent=2
            )

        print(
            f"\nTraining history saved to:\n"
            f"{history_path}"
        )

