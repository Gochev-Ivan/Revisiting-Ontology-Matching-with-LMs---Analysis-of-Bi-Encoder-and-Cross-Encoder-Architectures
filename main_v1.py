"""## Global Matching Eval"""

def PipelineEvaluation(df_matches, df_references, display_report=True, report_name=""):
    matches = set(
        (str(row['SrcEntity']), str(row['TgtEntity']))
        for row_idx, row in df_matches.iterrows()
    )
    gt = set(
        (str(row['SrcEntity']), str(row['TgtEntity']))
        for row_idx, row in df_references.iterrows()
    )

    tp_set = matches.intersection(gt)

    FP = matches - gt
    FN = gt - matches

    pd.DataFrame(tp_set, columns=['SrcEntity', 'TgtEntity']).to_csv(r"/content/TP.csv")
    pd.DataFrame(FP, columns=['SrcEntity', 'TgtEntity']).to_csv(r"/content/FP.csv")
    pd.DataFrame(FN, columns=['SrcEntity', 'TgtEntity']).to_csv(r"/content/FN.csv")

    tp = len(tp_set)
    fp = len(matches - gt)
    fn = len(gt - matches)

    precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
    recall = tp / (tp + fn) if (tp + fn) > 0 else 0.0
    f1 = 2 * (precision * recall) / (precision + recall) if (precision + recall) > 0 else 0.0

    lines = [
        "-" * 30,
        f"EVALUATION REPORT : " + report_name,
        "-" * 30,
        f"Ground Truth Matches:  {len(gt)}",
        f"Predicted Matches:     {len(matches)}",
        f"True Positives (TP):   {tp}",
        f"False Positives (FP):  {fp}",
        f"False Negatives (FN):  {fn}",
        "-" * 30,
        f"Precision:             {precision:.4f}",
        f"Recall:                {recall:.4f}",
        f"F1 Score:              {f1:.4f}",
        "-" * 30,
        f"=== END REPORT ===\n"
    ]

    if display_report:
      for line in lines:
          print(line)

    return {
        "Ground Truth Matches": len(gt),
        "Predicted Matches": len(matches),
        "TP": tp,
        "FP": fp,
        "FN": fn,
        "P": precision, "R": recall, "F1": f1
    }

"""## Local Matching Eval"""

def evaluate_retrieval(
    results_df: pd.DataFrame,
    ground_truth_df: pd.DataFrame
):
    gt_map = dict(
        zip(
            ground_truth_df["SrcEntity"],
            ground_truth_df["TgtEntity (True)"]
        )
    )

    top1 = results_df[results_df["rank"] == 1]

    top1_map = dict(
        zip(
            top1["SrcEntity"],
            top1["TgtEntity"]
        )
    )

    all_sources = set(gt_map.keys())

    tp = 0
    fp = 0
    fn = 0

    recall_at_1_hits = 0

    for src in all_sources:

        true_tgt = gt_map[src]

        pred_tgt = top1_map.get(src, None)

        # Recall@1
        if pred_tgt == true_tgt:
            recall_at_1_hits += 1
            tp += 1
        else:
            fp += 1
            fn += 1

    precision = tp / (tp + fp + 1e-9)
    recall = tp / (tp + fn + 1e-9)
    f1 = (2 * precision * recall) / (precision + recall + 1e-9)
    recall_at_1 = recall_at_1_hits / len(all_sources)

    return {
        "precision": precision,
        "recall": recall,
        "f1": f1,
        "recall@1": recall_at_1
    }

