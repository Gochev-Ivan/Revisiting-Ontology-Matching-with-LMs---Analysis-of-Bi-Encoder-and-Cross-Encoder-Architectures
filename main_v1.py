def save_json(output_dir, filename, data):
  """
  Save a list or dictionary to a json file.
  :param output_dir:
  :param filename:
  :param data:
  :return:
  """
  if isinstance(data, dict):
      with open(output_dir + "\\" + f"{filename}.json", 'w') as json_file:
          json.dump({str(key): str(val) for key, val in data.items()}, json_file, indent=4)

