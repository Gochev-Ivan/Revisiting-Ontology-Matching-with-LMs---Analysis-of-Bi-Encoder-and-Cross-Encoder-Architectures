def recall_at_1_sentence_transformer(
    alignments_df,
    gold_df
):
    gold_pairs = set(zip(
        gold_df["SrcEntity"],
        gold_df["TgtEntity"]
    ))

    ranked = (
        alignments_df
        .sort_values(
            ["SrcEntity", "score"],
            ascending=[True, False]
        )
    )

    top1 = (
        ranked
        .groupby("SrcEntity")
        .head(1)
    )

    hits = 0

    for _, row in top1.iterrows():

        pair = (
            row["SrcEntity"],
            row["TgtEntity"]
        )

        if pair in gold_pairs:
            hits += 1

    total = (
        gold_df["SrcEntity"]
        .nunique()
    )

    recall1 = hits / total

    return recall1

