def filter_tagged_alignments(df_src_ent, df_tgt_ent, df_matches, score_col):
  src_map = dict(zip(df_src_ent['EntityName'], df_src_ent['use_in_alignment']))
  tgt_map = dict(zip(df_tgt_ent['EntityName'], df_tgt_ent['use_in_alignment']))

  final_matches_data = []
  for _, row in tqdm(df_matches.iterrows(), total=len(df_matches), desc='Filtering Final Matches'):
    if src_map[row['SrcEntity']] and tgt_map[row['TgtEntity']]:
      final_matches_data.append({
          'SrcEntity': row['SrcEntity'],
          'TgtEntity': row['TgtEntity'],
          'Score': row[score_col]
      })
  return pd.DataFrame(final_matches_data)

"""## Preparing Ontology Entities Data"""

class OntologyEntitiesData:
  def __init__(self, df_entities_file, label_col, new_ent_col_name, new_label_col_name) -> None:
     self.label_col = label_col
     self.new_ent_col_name = new_ent_col_name
     self.new_label_col_name = new_label_col_name

     self.df_onto_entities = load_dataset(df_entities_file, 'csv')
     self.df_ent = self.read_onto_entities(label_col)

     self.df_ent = self.rename_columns(new_ent_col_name, new_label_col_name)

     self.max_len = self.max_label_length()

  def max_label_length(self):
    return max([len(x) for x in self.df_ent[self.new_label_col_name]])

  def read_onto_entities(self, label_col):
    self.df_onto_entities = self.df_onto_entities[self.df_onto_entities['EntityType'] == "class"]
    self.df_onto_entities['EntityName'] = self.df_onto_entities['EntityName'].apply(lambda x: str(x))
    self.df_onto_entities['EntityLabel (Extended)'] = self.df_onto_entities['EntityLabel (Extended)'].fillna(
        self.df_onto_entities['EntityLabel (Short)']
    )

    self.df_ent = self.df_onto_entities[['EntityName', label_col]]
    self.df_ent = self.df_ent[self.df_ent['EntityName'] != "Thing"]

    self.df_ent = self.df_ent.reset_index(drop=True)

    return self.df_ent

  def get_dfs(self):
    return self.df_ent, self.df_onto_entities, self.max_len

  def rename_columns(self, new_ent_col_name, new_label_col_name):
    return self.df_ent.rename(columns={"EntityName": new_ent_col_name, self.label_col: new_label_col_name})

